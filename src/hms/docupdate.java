    private void dusearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dusearchActionPerformed
        try {
            Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/hms", "root", "");

            String sql = "select * from doctor_db where email= '" + duemail.getText() + "'and password='" + dupass.getText() + "' ";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                duregn.setText(rs.getString("regno"));
                duname.setText(rs.getString("name"));

                dufather.setText(rs.getString("fathername"));
                dugender.setText(rs.getString("gender"));

                duspecialist.setSelectedItem(rs.getString("specialist"));
                duvillage.setText(rs.getString("village"));

                duphone.setText(rs.getString("phone"));
                duvhour.setText(rs.getString("vhours"));
                dubranch.setText(rs.getString("branch"));
            } else {
                JOptionPane.showMessageDialog(null, "This Email is not Registered");
                duemail.setText("");
                dupass.setText("");
            }

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_dusearchActionPerformed

