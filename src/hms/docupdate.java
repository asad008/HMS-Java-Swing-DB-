                duvillage.setText("");
                duphone.setText("");
                duemail.setText("");

                duregn1.setText("");
                duname1.setText("");
                dupass1.setText("");
                dufather1.setText("");
                dugender1.setText("");
                duname1.setText("");
                dupass1.setText("");
                duspecialist1.setSelectedIndex(0);
