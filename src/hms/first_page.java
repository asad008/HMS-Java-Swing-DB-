    private void docsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_docsearchActionPerformed
        // TODO add your handling code here:

        try {
            Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/hms", "root", "");

            String sql = "select * from doctor_db where name= '" + docname.getText() + "'and specialist='" + docspecialist.getSelectedItem() + "' ";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                docvigi.setText(rs.getString("vhours"));
            } else {
                JOptionPane.showMessageDialog(null, "Not Found");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_docsearchActionPerformed

